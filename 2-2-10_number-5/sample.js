var x = 10;
var y = 4;
var result;

// 足し算
result = x + y;
console.log(result); //=> 14

// 引き算
result = x - y;
console.log(result); //=> 6

// 掛け算
result = x * y;
console.log(result); //=> 40

// 割り算（商）
result = x / y;
console.log(result); //=> 2.5

// 割り算（あまり）
result = x % y;
console.log(result); //=> 2
